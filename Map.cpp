Map::Map()
{
	dirt = TextureManager::LoadTexture("DirtTileSprite.png");
	grass = TextureManager::LoadTexture("GrassTileSprite.png");
	water = TextureManager::LoadTexture("WaterTileSprite.png");

	LoadMap(firstCircle);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; ++row)
	{
		for (int col = 0; col < 25; ++col)
		{
			map[row][col] = arr[row][col];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;
	for (int row = 0; row < 20; ++row)
	{
		for (int col = 0; col < 25; ++col)
		{
			type = map[row][col];
			
			dest.x = col * 32;
			dest.y = row * 32;

			switch (type)
			{
				case 0:
					TextureManager::Draw(water, src, dest);
					break;
				case 1:
					TextureManager::Draw(grass, src, dest);
					break;
				case 2:
					TextureManager::Draw(dirt, src, dest);
					break;
				default:
					break;
			}
		}
	}
}